    private void btnkirimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnkirimActionPerformed
        // TODO add your handling code here:
        try {
            String kalimat = txtjawaban.getText();
            String[] kata = kalimat.split("\\s");
            int nilai = 0;
            String siqil = "select * from mkbd_soal";
            con=DriverManager.getConnection("jdbc:mysql://localhost/db_essay","root","");
            st=con.createStatement();
            res=st.executeQuery(siqil);
            if(res.next() && res.getString(1).equals(txtmk.getSelectedItem())){
                for (int i = 0; i < kata.length; i++) {
                    for(int j=3; j<7;j++) {
                        if(kata[i].contains(res.getString(j))){
                            nilai++;
                        }
                    }
                }
            }
            String sql = "INSERT INTO mkbd_jawaban VALUES ('"+txtnama.getText()+"','"+txtnim.getText()+"','"+txtmk.getSelectedItem()+"','"+txtjawaban.getText()+"','"+nilai+"')";
            con = DriverManager.getConnection("jdbc:mysql://localhost/db_essay","root","");
            st = con.createStatement();
            java.sql.PreparedStatement
            pst=con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnkirimActionPerformed

