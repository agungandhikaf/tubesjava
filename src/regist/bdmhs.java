    private void load_table(){
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Matakuliah");
        model.addColumn("Soal");
        
        //menampilkan data database kedalam tabel
        try {
            int no=1;
            String sql = "select * from mkbd_soal";
            con=DriverManager.getConnection("jdbc:mysql://localhost/db_essay","root","");
            st=con.createStatement();
            res=st.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});
            }
            tabel.setModel(model);
        } catch (SQLException e) {
        }
    }    

